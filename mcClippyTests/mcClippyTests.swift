        // (If AX is untrusted we hit the trust gate and also return false.)
        #expect(AutoPaster.paste(into: nil) == false)
    }

    @MainActor
    @Test func globalShortcutReregistersOnWakeNotification() async throws {
        let manager = GlobalShortcutManager.shared
        manager.start()
        let initialStatus = manager.registrationStatus

        // Posting the system wake notification should trigger reload().
        NSWorkspace.shared.notificationCenter.post(
            name: NSWorkspace.didWakeNotification,
            object: nil
        )
        // Let the main queue process the observer.
        try await Task.sleep(for: .milliseconds(50))

        // After reload, the status should not regress from registered.
        // (If the test host couldn't register the default shortcut initially —
        // e.g. another app already owns ⇧⌘V — we just verify the handler ran.)
        if initialStatus.isRegistered {
            #expect(manager.registrationStatus.isRegistered)
        }
    }

    @MainActor
    @Test func rebootRetentionClearsRegularAndPinnedItemsByDefault() throws {
        let settings = HistorySettings.shared
        let originalRegularRetention = settings.regularRetentionPolicy
        let originalPinnedRetention = settings.pinnedRetentionPolicy
        defer {
            settings.regularRetentionPolicy = originalRegularRetention
            settings.pinnedRetentionPolicy = originalPinnedRetention
            settings.resetRebootScopedHistoryMarkerForTesting()
        }

        settings.regularRetentionPolicy = .untilReboot
        settings.pinnedRetentionPolicy = .untilReboot
        settings.resetRebootScopedHistoryMarkerForTesting()

        let schema = Schema([Item.self])
        let config = ModelConfiguration(schema: schema, isStoredInMemoryOnly: true)
        let container = try ModelContainer(for: schema, configurations: [config])
        let context = container.mainContext

        context.insert(Item(
            type: .text,
            plainTextPreview: "regular",
            dataBlob: Data("regular".utf8),
            contentHash: "regular",
            sourceAppBundleId: nil,
            sourceAppName: nil,
            sizeBytes: 7
        ))
        context.insert(Item(
            type: .text,
            plainTextPreview: "pinned",
            dataBlob: Data("pinned".utf8),
            contentHash: "pinned",
            sourceAppBundleId: nil,
            sourceAppName: nil,
            isPinned: true,
            sizeBytes: 6
        ))
        try context.save()

        PasteboardMonitor.shared.start(modelContainer: container)

        #expect(try context.fetch(FetchDescriptor<Item>()).isEmpty)
    }
