        #expect(PasteboardSerializer.isPasswordManagerSource("com.apple.Passwords"))
        #expect(!PasteboardSerializer.isPasswordManagerSource("com.apple.Safari"))
        #expect(!PasteboardSerializer.isPasswordManagerSource(nil))
    }

